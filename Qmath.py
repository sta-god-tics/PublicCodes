import math as mt
print(mt.pi)                         #returns the value of pi
print(mt.e)                          #returns the value of e
print(mt.floor(25.63))               #returns greatest int less than input 
print(mt.ceil(25.63))                #returns lowest int greater than input
print(mt.factorial(5))               #returns factorial
print(mt.lcm(12,4,24,10))            #returns lcm of multiple input
print(mt.gcd(12,4,24,10))            #returns gcd of multiple input
print(mt.comb(10,2))                 #returns no of ways of possible combination nCr
print(mt.perm(10,2))                 #returns no of ways of possible permutation nPr
print(mt.sqrt(38))                   #returns square root of input
print(mt.log(64,2))                  #returns log64 with base 2
print(mt.pow(10,5))                  #returns 10 to the power 5
print(mt.cos(0))                     #returns cosine of input
print(mt.sin(0))                     #returns sine of input
print(mt.tan(0))                     #returns tan of input
print(mt.asin(0))                    #returns inverse of sin of input
print(mt.acos(0))                    #returns inverse of cos of input
print(mt.atan(0))                    #returns inverse of tan of input
print(mt.degrees(100))               #returns degree of radian input
print(mt.radians(100))               #returns radian of degree input